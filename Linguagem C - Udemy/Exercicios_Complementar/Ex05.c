// Exercicios gerados pelo Google Gemini - Contador

#include <stdio.h>

int main() {
    int numero, contador;

    printf("Digite algum valor inteiro: ");
    scanf("%d", &numero);

    contador = 0;

    while (numero >= contador) {
        printf("CONTADOR = %d\n", contador);
        contador = contador + 1;
    }

    return 0;
}