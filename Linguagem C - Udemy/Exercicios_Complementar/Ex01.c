#include <stdio.h>

int main() {
    double num, num2, adicao, sub, multi, divi;

    printf("Informe o primeiro valor: ");
    scanf("%lf", &num);
    printf("Informe o segundo valor: ");
    scanf("%lf", &num2);

    adicao = num + num2;
    sub = num - num2;
    multi = num * num2;
    divi = num / num2;

    printf("ADICAO = %.1lf\n", adicao);
    printf("SUBTRACAO = %.1lf\n", sub);
    printf("MULTIPLICACAO = %.1lf\n", multi);
    printf("DIVISAO = %.1lf\n", divi); 

    return 0;
}